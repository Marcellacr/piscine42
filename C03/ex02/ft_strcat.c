#include <unistd.h>

char	*ft_strcat(char *dest, char *src)

{
	int	i;
	int	destlen;

	i = 0;
	destlen = 0;
	while (dest[destlen])
	{
		destlen++;
	}
	while (src[i])
	{
		dest[destlen + i] = src[i];
		i++;
	}
	dest[destlen + i] = '\0';
	return (dest);
}
